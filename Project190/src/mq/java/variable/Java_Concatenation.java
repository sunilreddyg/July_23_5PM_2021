public class Java_Concatenation {

	public static void main(String[] args) {
		
		

		int a=100;
		int b=200;
		
		int x=a+b;			//Here + symbol conducting addition
		System.out.println("total value is -> "+x);		//Here + club two value
		System.out.println("total value is-> "+a+b);    //It club all numbers and characters
		System.out.println("total value is-> "+(a+b));  //here a+b conduct addition because it is defined between two cirlce braces

	}

}
